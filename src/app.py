import math


def add(a, b):
    return a + b


def sub(a, b):
    return a - b


def mul(a, b):
    return a * b


def div(a, b):
    return a / b


def log(n):
    return math.log(n)


def cos(n):
    return math.cos(n)


def sqrt(n):
    return math.sqrt(n)


def percentage(a, b):
    return a / b * 100
